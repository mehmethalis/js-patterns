export function sum(num1, num2) {
  return num1 + num2;
}

export function subtract(num1, num2) {
  return num1 + num2;
}

export function divide(num1, num2) {
  return num1 / num2;
}

export function multiply(num1, num2) {
  return num1 * num2;
}

function mode(num1, num2) {
  return num1 % num2;
}

